# Operasi Komparasi

a = 5
b = 3

print("")

# Lebih besar dari > 
print("===== Lebih Besar Dari (>) =====")
hasil = a > 3
print(a, '>', 3, '=', hasil)
hasil = b > 3
print(b, '>', 3, '=', hasil)

print("")

# Kurang dari < 
print("===== Kurang Dari (<) =====")
hasil_kurang = a < 3
print(a, '<', 3, '=', hasil_kurang)
hasil_kurang = b < 3
print(b, '<', 3, '=', hasil_kurang)

print("")

# lebih besar sama dengan >=
print("===== Lebih Besar Sama Dengan (>=) =====")
hasil = a >= 3
print(a,'>=', 3, '=', hasil)
hasil = b >= 3
print(b,'>=', 3, '=', hasil)

print("")

# Kurang dari sama dengan <=
print("===== Kurang Dari Sama Dengan (<=) =====")
hasil = a <= 3
print(a, '<=', 3, '=', hasil)
hasil = b <= 3
print(b, '<=', 3, '=', hasil)
