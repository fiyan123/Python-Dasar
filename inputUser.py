# Cara Input Untuk User

# data yang diinputkan user pasti string
data = input("Masukkan Data : ")
print("Data Yang Dimasukkkan : ", data, "Bertype = ", type(data))

# Data integer atau float
angka = int(input("Masukkan Angka : "))
print("Angka Yang Dimasukkkan : ", angka, "Bertype = ", type(angka))

# Data Boolean
boolean = bool(int(input("Masukkan Nilai Boolean : ")))
print("Nilai Boolean Yang Dimasukkkan : ", boolean, "Bertype = ", type(boolean))

